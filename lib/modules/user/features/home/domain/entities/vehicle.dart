class Vehicle {
  final int id;
  final String image;
  final int vehicleType;
  final String model;
  final int numOfPass;
  final int numOfBags;

  Vehicle({
    required this.id,
    required this.image,
    required this.vehicleType,
    required this.model,
    required this.numOfPass,
    required this.numOfBags,
  });
}
